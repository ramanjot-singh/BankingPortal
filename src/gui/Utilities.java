    private void descriptionTFKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_descriptionTFKeyPressed
        if (evt.getKeyCode() == 10) {
            int i = JOptionPane.showConfirmDialog(this, "Do you want to update", "About Account MAnagement", JOptionPane.YES_NO_CANCEL_OPTION);
            if (i == 0) {
                try {
                    Connection c3 = DBConnection.getConnection();
                    String query = "update user_account set description=? where account_no=? and user_id=?";
                    PreparedStatement psmt = c3.prepareStatement(query);
                    psmt.setString(1, descriptionTF.getText());
                    psmt.setString(2, jComboBox2.getSelectedItem().toString());
                    psmt.setString(3, user);
                    psmt.executeUpdate();
                    JOptionPane.showMessageDialog(this, "Updated", "About Description", JOptionPane.INFORMATION_MESSAGE);
                } catch (Exception ex) {
                    ex.printStackTrace();
                }

            } else {
                JOptionPane.showMessageDialog(this, "Not Updated", "About Description", JOptionPane.INFORMATION_MESSAGE);
                descriptionTF.setText(descript);
            }
        }
    }//GEN-LAST:event_descriptionTFKeyPressed

