    private void SessionBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SessionBtnActionPerformed

        try {
            Connection con = DBConnection.getConnection();
            String query = "select * from user_session where user_id=?";
            PreparedStatement psmt = con.prepareStatement(query);
            psmt.setString(1, user);
            ResultSet rs = psmt.executeQuery();
            ResultSetMetaData rsmd;
            rsmd = rs.getMetaData();
            Vector column = new Vector();
            int count = rsmd.getColumnCount();
            DefaultTableModel dtm = new DefaultTableModel();
            for (int i = 1; i <= count; i++) {
                column.addElement(rsmd.getColumnName(i));
            }
            dtm.setColumnIdentifiers(column);
            while (rs.next()) {
                Vector row = new Vector();
                for (int i = 1; i <= count; i++) {
                    row.addElement(rs.getString(i));
                }
                dtm.addRow(row);
            }
            jTable1.setModel(dtm);
        } catch (Exception ex) {
            ex.printStackTrace();
        }

        jSplitPane1.setRightComponent(SessionTable);
        SessionTable.setVisible(true);
    }//GEN-LAST:event_SessionBtnActionPerformed

