    private void setPath(File file) {
        try {
            //System.out.println(displayImgLbl.getSize());
            displayImgLbl.setSize(135, 143);
            ImageIcon icon = ResizeImage.scaleTo(file, displayImgLbl.getSize());

            if (icon != null) {
                displayImgLbl.setText("");
                displayImgLbl.setIcon(icon);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

