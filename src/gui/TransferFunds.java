    private void submitBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitBtnActionPerformed
        try {
            int row = jTable1.getRowCount();
            int col = jTable1.getColumnCount();
            for (int i = 0; i < row; i++) {
                int j = 0;
                while (j < col) {

                    /* get balance earlier of credit account*/ int pdbalance = Integer.parseInt((String) jTable1.getValueAt(i, j));
                    int pcbalance = Integer.parseInt((String) jTable1.getValueAt(i, ++j));
                    int transamount = Integer.parseInt((String) jTable1.getValueAt(i, ++j));

                    String ndbalance = String.valueOf(pdbalance - transamount);
                    String ncbalance = String.valueOf(pcbalance + transamount);
                    query = "update admin set balance='" + ndbalance + "' where account_no='" + (String) jTable1.getValueAt(i, ++j) + "'";
                    psmt = con.prepareStatement(query);
                    psmt.executeUpdate();

                    query = "update admin set balance='" + ncbalance + "' where account_no='" + oneCreditCB.getSelectedItem().toString() + "'";
                    psmt = con.prepareStatement(query);
                    psmt.executeUpdate();
                    System.out.println(transamount);
                    System.out.println(ndbalance);
                    System.out.println(ncbalance);
                }

            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_submitBtnActionPerformed

