    private void sTransferBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sTransferBtnActionPerformed

        try {
            query = "select account_no from user_account where user_id='" + user + "'";
            stm = con.createStatement();
            ResultSet rrs = stm.executeQuery(query);

            DefaultComboBoxModel model = (DefaultComboBoxModel) oneDebitCB.getModel();
            DefaultComboBoxModel model2 = (DefaultComboBoxModel) oneCreditCB.getModel();
            // oneDebitCB.removeAllItems();
            while (rrs.next()) {
                String acc = rrs.getString("account_no");

                model.addElement(acc);
                model2.addElement(acc);
            }
            oneDebitCB.setModel(model);
            oneCreditCB.setModel(model2);
            rrs.close();
            stm.close();

            jSplitPane1.setRightComponent(aa);

        } catch (Exception ex) {
            ex.printStackTrace();
        }

    }//GEN-LAST:event_sTransferBtnActionPerformed

